// Console.log the values of each property of "kitten"
// /*
//     Console.log the values of each property of "kitten"
// */

let kitten = {
  ageMonths: 3,
  isFemale: true,
  furColour: "brown",
};


//dot notation
console.log(kitten.ageMonths);
console.log(kitten.isFemale);
console.log(kitten.furColour);

//bracket notation 
console.log(kitten["ageMonths"]);
console.log(kitten["isFemale"]);
console.log(kitten["furColour"]);
